# 11a. cal1.py contains add1(x,y), sub1(x,y), mul1(x,y), div1(x,y), mod1(x,y), pow1(x,y)
def add1(x, y):
    return x + y

def sub1(x, y):
    return x - y

def mul1(x, y):
    return x * y

def div1(x, y):
    return x / y

def mod1(x, y):
    return x % y

def pow1(x, y):
    return x ** y
